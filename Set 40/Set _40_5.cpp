//Write a program to illustrate the use of shortcut assignment for arithmetic operators.
#include<iostream>
using namespace std;
int main()
{
    int a=20;
    a+=20;
    cout<<a<<endl;
    a-=35;
    cout<<a<<endl;
    a*=10;
    cout<<a<<endl;
    a/=7;
    cout<<a<<endl;
    a%=7;
    cout<<a<<endl;
}
